//! # ForgePass Shared Contract Types
//!
//! Single source of truth for all `#[contracttype]` structs, enums, and the
//! `ContractError` enum shared across all four ForgePass Soroban contracts.
//!
//! **ABI authority:** `contracts/INTERFACES.md` (Issue #014, complete)
//!
//! Import this crate in each contract crate's `Cargo.toml`:
//! ```toml
//! forgepass-shared = { path = "../shared" }
//! ```
//!
//! Do **not** add fields or variants without a corresponding update to
//! `INTERFACES.md` and a coordinated WASM upgrade across all four contracts.

#![no_std]

use soroban_sdk::{contracterror, contracttype, Address, BytesN, String};

