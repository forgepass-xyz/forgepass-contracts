/// Contribution signal source types.
///
/// **v1 active:** `GithubPr`, `SorobanContract`, `StellarDex`, `Hackathon`.
/// **Reserved:** `ScfGrant`, `GrantfoxBounty`, `TrustlessWork` -- appended via
/// WASM upgrade when the corresponding partnership is confirmed (issue #009).
///
/// **Contracts never branch on `SignalType` in function bodies.** All signal-
/// type-specific logic lives in the NestJS scoring engine (off-chain). New
/// variants can be appended without any credential-store contract logic change.
///
/// ABI: `INTERFACES.md` Section 3.6
#[contracttype]
#[derive(Clone)]
pub enum SignalType {
    /// GitHub pull requests merged into admin-registered Stellar ecosystem repos.
    GithubPr,
    /// Soroban smart contract deployments from the contributor's wallet.
    SorobanContract,
    /// DEX trades and Aquarius LP positions via Stellar Horizon.
    StellarDex,
    /// Hackathon participation records ingested via admin batch upload.
    Hackathon,
    // --- Reserved: no v1 contract logic. Activated via WASM upgrade once the
    // --- corresponding partnership is confirmed via issue #009 (FR-09-B).
    /// Reserved: SCF grant history. Gated on #009 confirmation + issue R07.
    ScfGrant,
    /// Reserved: GrantFox bounty completions. Gated on #009 confirmation + R05.
    GrantfoxBounty,
    /// Reserved: Trustless Work milestone completions. Gated on #009 + R06.
    TrustlessWork,
}

/// Achievement badge milestone types.
///
/// **v1 active (7):** `FirstPr`, `FirstContract`, `HackathonParticipant`,
/// `RisingContributor`, `MultiRepoContributor`, `FirstSorobanInvocation`,
/// `FullStackBuilder`.
/// **Reserved (3):** `FirstBounty`, `FirstGrant`, `FirstTrustlessWork`.
/// Trigger criteria: `contracts/badges/milestone-registry.json` (issue #008).
///
/// **The soulbound-nft contract never branches on `MilestoneType` in function
/// bodies.** New variants can be appended via WASM upgrade without logic changes.
///
/// ABI: `INTERFACES.md` Section 3.7
#[contracttype]
#[derive(Clone)]
pub enum MilestoneType {
    /// First merged PR (author) into a registered Stellar ecosystem repo.
    FirstPr,
    /// First Soroban contract deployed by the contributor's wallet.
    FirstContract,
    /// Verified participation in an admin-registered Stellar hackathon event.
    HackathonParticipant,
    /// 10+ merged PRs (author) across registered Stellar repos. Compound badge.
    RisingContributor,
    /// Merged PRs across 3+ distinct registered Stellar repos. Compound badge.
    MultiRepoContributor,
    /// First invocation of any contract deployed by the wallet (any caller).
    FirstSorobanInvocation,
    /// At least 1 `GithubPr` credential AND 1 `SorobanContract` credential.
    FullStackBuilder,
    // --- Reserved: no v1 contract logic. Activated via WASM upgrade when the
    // --- corresponding signal source is confirmed and live.
    /// Reserved: first GrantFox bounty. Gated on #009 confirmation + R05.
    FirstBounty,
    /// Reserved: first SCF grant. Gated on #009 confirmation + R07.
    FirstGrant,
    /// Reserved: first Trustless Work milestone. Gated on #009 confirmation + R06.
    FirstTrustlessWork,
}

