/// On-chain passport record. One per contributor, keyed by wallet `Address`.
///
/// Stored in **persistent** storage in the passport contract so it survives
/// ledger entry TTL expiry (FR-02.3 non-revocability). TTL extension is a
/// backend concern handled by the `OnchainWriterService` (issue #027).
///
/// ABI: `INTERFACES.md` Section 3.1
#[contracttype]
#[derive(Clone)]
pub struct PassportRecord {
    /// Stellar public key. Primary key for this passport record.
    pub wallet: Address,
    /// CIDv1 of the IPFS/Arweave metadata object. Non-empty; max 100 chars.
    /// Raw PII is **never** stored on-chain -- only the content-addressed hash
    /// (FR-02.5). The `StorageService` in issue #044 handles the off-chain pin.
    pub ipfs_cid: String,
    /// Unix timestamp (seconds) of passport creation.
    /// Always sourced from `env.ledger().timestamp()` inside the contract --
    /// never from a caller-supplied argument (prevents backdating).
    pub created_at: u64,
    /// Admin-set sybil flag. Default: `false`.
    /// Toggled only via `set_sybil_flag`. A flagged passport remains on-chain
    /// (FR-02.3); the API layer applies the read exclusion (FR-11.1).
    pub sybil_flagged: bool,
}

/// On-chain credential proof anchoring a contribution signal to a passport.
///
/// Stored in **persistent** storage in the credential-store contract.
/// Live credentials (up to 100 per wallet) are stored as a
/// `Vec<CredentialRecord>` under `DataKey::Credentials(wallet)`.
/// Archived credentials are removed via `remove_credentials` and replaced by
/// an `ArchiveRecord` Merkle root.
///
/// ABI: `INTERFACES.md` Section 3.2
#[contracttype]
#[derive(Clone)]
pub struct CredentialRecord {
    /// Contract-generated monotonic ID. Never reuses IDs, even after archival.
    /// Generated by a per-wallet counter in instance storage.
    pub id: u64,
    /// FK to `PassportRecord.wallet`.
    pub wallet: Address,
    /// Contribution signal type. v1 active variants only (reserved variants
    /// are defined in `SignalType` and are appended via WASM upgrade).
    pub signal_type: SignalType,
    /// External reference ID paired with `signal_type` as the deduplication key.
    /// `(signal_type, source_id)` must be unique in the live credential set.
    pub source_id: String,
    /// Unix timestamp (seconds) of the original contribution event.
    pub event_date: u64,
    /// SHA-256 hex string of the raw signal data. Always 64 chars.
    pub data_hash: String,
}

/// Versioned Trust Score snapshot, anchored on-chain after each recalculation.
///
/// Stored in **persistent** storage as a bounded `Vec<ScoreSnapshot>` (max 50)
/// per wallet under `DataKey::ScoreHistory(wallet)` in the trust-score contract.
/// The latest snapshot is also mirrored to instance storage under
/// `DataKey::CurrentScore(wallet)` for O(1) reads.
///
/// ABI: `INTERFACES.md` Section 3.3
#[contracttype]
#[derive(Clone)]
pub struct ScoreSnapshot {
    /// Trust Score value. Always in range [0, 100].
    /// `anchor_score` rejects values outside this range with `InvalidScore` (400).
    pub score: u32,
    /// Semver string of the scoring algorithm, e.g. `"1.0"`.
    pub algorithm_version: String,
    /// SHA-256 hex string of the input signal set used for this computation.
    pub signal_hash: String,
    /// Unix timestamp (seconds) of when the score was computed off-chain.
    pub computed_at: u64,
}

/// Soulbound achievement badge NFT record. Non-transferable by design.
///
/// At most one `BadgeRecord` per `(wallet, MilestoneType)` pair.
/// Stored in **persistent** storage in the soulbound-nft contract under
/// `DataKey::Badge(badge_id)`.
///
/// ABI: `INTERFACES.md` Section 3.4
#[contracttype]
#[derive(Clone)]
pub struct BadgeRecord {
    /// Contract-generated **global** monotonic ID across all wallets.
    /// Never reused. Starts at 1, increments on every successful `mint`.
    pub badge_id: u64,
    /// Badge holder wallet address.
    pub wallet: Address,
    /// Milestone type. At most one `BadgeRecord` per `(wallet, MilestoneType)` pair.
    pub milestone_type: MilestoneType,
    /// CID of the badge metadata JSON on IPFS/Arweave.
    pub ipfs_cid: String,
    /// Unix timestamp (seconds) of the `mint` call.
    pub minted_at: u64,
}

/// On-chain Merkle root proof anchored after each credential archival cycle.
///
/// Stored in **persistent** storage in the credential-store contract.
/// One entry per archival cycle per wallet, keyed by
/// `DataKey::ArchiveRecord(wallet, archive_index)`.
/// `archive_index` is a monotonically increasing `u32` per wallet, starting at 0,
/// managed in instance storage under `DataKey::ArchiveIndex(wallet)`.
///
/// Full archival workflow: `contracts/ARCHITECTURE.md` Section 4.
///
/// ABI: `INTERFACES.md` Section 3.5
#[contracttype]
#[derive(Clone)]
pub struct ArchiveRecord {
    /// 32-byte SHA-256 Merkle root of the archived credential batch.
    /// Must match the `merkle_root` field in the IPFS archive JSON file.
    pub merkle_root: BytesN<32>,
    /// Number of credentials in this specific archive batch.
    pub credential_count: u32,
    /// Unix timestamp (seconds) of when `add_archive_record` was called.
    pub archived_at: u64,
    /// CID of the archive JSON on IPFS/Arweave.
    /// Stored on-chain so third parties can verify without querying ForgePass API.
    pub ipfs_cid: String,
}

