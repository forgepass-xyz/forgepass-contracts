/// Unified error enum for all four ForgePass Soroban contracts.
///
/// Discriminants are grouped by hundreds so the originating contract is
/// identifiable from error codes in NestJS logs and Horizon transaction results.
///
/// **Auth failure model:** auth failures on admin-only and owner-only functions
/// are Soroban **host traps** (`INVOKE_HOST_FUNCTION_TRAPPED`), NOT
/// `ContractError` returns. `require_auth()` in SDK `22.0.11` traps on failure.
/// `Unauthorized` (102) is a semantic marker reserved for future use -- no v1
/// function returns it.
///
/// ABI: `INTERFACES.md` Section 3.8
#[contracterror]
#[derive(Copy, Clone, Debug, PartialEq, Eq)]
pub enum ContractError {
    // 100s -- Initialisation (all four contracts)
    /// `initialize` called a second time. Admin address is unchanged.
    AlreadyInitialized = 100,
    /// Admin-only function called before `initialize` has stored the admin address.
    NotInitialized = 101,
    /// Semantic marker. No v1 function returns this -- auth failures are host traps.
    Unauthorized = 102,

    // 200s -- Passport contract
    /// `update_metadata_cid` or `set_sybil_flag`: no passport exists for this wallet.
    PassportNotFound = 200,
    /// `create_passport`: wallet already has a passport. Idempotency-safe return --
    /// the original record is unchanged; this is not a panic.
    PassportAlreadyExists = 201,

    // 300s -- Credential store contract
    /// `add_credential`: `(signal_type, source_id)` pair already exists in live set.
    CredentialAlreadyExists = 300,
    /// Reserved. No v1 function returns this variant.
    CredentialNotFound = 301,
    /// `remove_credentials`: called before `add_archive_record` -- prevents
    /// credential deletion without a corresponding on-chain Merkle root proof.
    ArchiveRecordRequired = 302,

    // 400s -- Trust Score contract
    /// `anchor_score`: score value is outside the valid 0-100 range.
    InvalidScore = 400,
    /// `anchor_score` defensive guard: snapshot count exceeded 50 before cap
    /// management ran. Indicates a backend implementation bug, not a user error.
    HistoryCapExceeded = 401,

    // 500s -- Soulbound NFT contract
    /// `mint`: wallet already holds a badge for this `MilestoneType`.
    BadgeAlreadyMinted = 500,
    /// Reserved. `get_badge` returns `Option<BadgeRecord>` -- not this error.
    BadgeNotFound = 501,
    /// **Test harness only.** No transfer function exists in the soulbound-nft
    /// contract. This variant provides a runtime-verifiable proof of
    /// non-transferability alongside the interface-level proof.
    TransferNotAllowed = 502,
}
